/// TrUAPI Dart host.
///
/// The host side of the TrUAPI protocol, for host applications (e.g. a Flutter
/// desktop host) that implement the services the products inside them call.
/// Products are web apps and use the TS/JS `@parity/truapi` client, so this Dart
/// package ships **only** the host.
///
/// Implement the generated `TruapiHostHandlers` (one typed handler group per
/// service) and wire them to a [Provider] with `createTruapiServer`:
///
/// ```dart
/// import 'package:truapi/truapi.dart';
///
/// final server = createTruapiServer(provider, MyHostHandlers());
/// // ... later:
/// server.dispose();
/// ```
///
/// Handlers receive and return the inner (selected-version) protocol types; the
/// generated dispatch entries handle versioned wire wrapping, SCALE
/// encode/decode, and the request/subscription frame lifecycle.
library;

export 'src/result.dart';
export 'src/scale.dart' show Codec, Input, Unit, unitValue;
export 'src/transport.dart'
    show
        Provider,
        ProtocolMessage,
        RequestFrameIds,
        SubscriptionFrameIds,
        SubscriptionInterrupted,
        decodeWireMessage,
        encodeWireMessage;
export 'src/host/host_server.dart';
export 'src/providers/loopback_provider.dart';
// Generated by `truapi-codegen`:
export 'src/generated/index.dart'; // types + wire table
export 'src/generated/host.dart';
