#!/usr/bin/env node
import { readdir, readFile, writeFile, mkdir } from "node:fs/promises";
import { join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = fileURLToPath(new URL("..", import.meta.url));
const DIST = join(ROOT, "js/packages/truapi/dist");
const OUT_DIR = join(ROOT, "js/packages/truapi/src/playground/codegen");
const OUT = join(OUT_DIR, "truapi-dts.ts");

async function* walk(dir) {
  for (const entry of await readdir(dir, { withFileTypes: true })) {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) yield* walk(path);
    else if (
      entry.isFile() &&
      path.endsWith(".d.ts") &&
      !entry.name.startsWith("truapi-dts")
    ) {
      yield path;
    }
  }
}

// Relative cross-file imports/exports inside the concatenated bundle have no
// real target once everything is wrapped in a single `declare module
// "@parity/truapi"` block — strip them so the local declarations elsewhere in
// the bundle take effect. External imports (e.g. neverthrow) are left alone.
const RELATIVE_IMPORT_RE =
  /^(?:import|export)[^;]*?from\s+["'](?:\.\.?\/)[^"']*["'];?\s*\n?/gm;

const chunks = [];
for await (const path of walk(DIST)) {
  const text = (await readFile(path, "utf8")).replace(RELATIVE_IMPORT_RE, "");
  const rel = relative(DIST, path);
  chunks.push(`// ${rel}\n${text}`);
}

const bundled = chunks.join("\n\n");
const escaped = bundled
  .replace(/\\/g, "\\\\")
  .replace(/`/g, "\\`")
  .replace(/\$\{/g, "\\${");

await mkdir(OUT_DIR, { recursive: true });
await writeFile(
  OUT,
  `// Auto-generated by scripts/bundle-truapi-dts.mjs. Do not edit.\nexport const truapiDts = \`${escaped}\`;\n`,
);

console.log(`wrote ${relative(ROOT, OUT)} (${bundled.length} chars from ${chunks.length} files)`);
