#!/usr/bin/env node
// Regenerates js/packages/truapi/src/explorer/versions.ts so that:
//   - the `main` entry points at the live `playground/codegen/services.ts`
//     and `explorer/codegen/types.ts`,
//   - every directory under `explorer/codegen/versions/*` is registered as a
//     historical entry (newest semver first).
//
// Idempotent: safe to run whenever package.json or the snapshots directory
// changes. Reads the active `main` version from package.json so the
// `packageVersion` constant stays in sync without a manual edit.

import { readFileSync, writeFileSync, readdirSync, existsSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const PKG_ROOT = resolve(HERE, "..", "js", "packages", "truapi");
const EXPLORER_DIR = resolve(PKG_ROOT, "src", "explorer");
const SNAPSHOTS_DIR = resolve(EXPLORER_DIR, "codegen", "versions");
const TARGET = resolve(EXPLORER_DIR, "versions.ts");

function semverParts(id) {
  const m = id.match(/^(\d+)\.(\d+)\.(\d+)(?:-(.+))?$/);
  if (!m) return null;
  return [Number(m[1]), Number(m[2]), Number(m[3]), m[4] ?? ""];
}

function compareSemverDesc(a, b) {
  const pa = semverParts(a);
  const pb = semverParts(b);
  if (!pa && !pb) return a.localeCompare(b);
  if (!pa) return 1;
  if (!pb) return -1;
  for (let i = 0; i < 3; i++) {
    if (pa[i] !== pb[i]) return pb[i] - pa[i];
  }
  // Semver: a version without a pre-release outranks one with a pre-release;
  // among pre-releases, lexically higher wins (rc > beta > alpha).
  const preA = pa[3] || null;
  const preB = pb[3] || null;
  if (preA === null && preB === null) return 0;
  if (preA === null) return -1;
  if (preB === null) return 1;
  return preB.localeCompare(preA);
}

const pkg = JSON.parse(readFileSync(resolve(PKG_ROOT, "package.json"), "utf8"));
const packageVersion = pkg.version;

const snapshotIds = existsSync(SNAPSHOTS_DIR)
  ? readdirSync(SNAPSHOTS_DIR, { withFileTypes: true })
      .filter((d) => d.isDirectory())
      .map((d) => d.name)
      .filter((name) => {
        const dir = resolve(SNAPSHOTS_DIR, name);
        const hasServices = existsSync(resolve(dir, "services.ts"));
        const hasTypes = existsSync(resolve(dir, "types.ts"));
        if (hasServices && hasTypes) return true;
        if (hasServices || hasTypes) {
          console.warn(
            `regen-explorer-versions: skipping incomplete snapshot ${dir} ` +
              `(missing ${hasServices ? "types.ts" : "services.ts"})`,
          );
        }
        return false;
      })
      .sort(compareSemverDesc)
  : [];

const imports = [
  `import { services as mainServices } from "../playground/codegen/services.js";`,
  `import { types as mainTypes } from "./codegen/types.js";`,
  ...snapshotIds.flatMap((id, i) => [
    `import { services as v${i}Services } from "./codegen/versions/${id}/services.js";`,
    `import { types as v${i}Types } from "./codegen/versions/${id}/types.js";`,
  ]),
];

const entries = [
  `  { id: "main", services: mainServices, types: mainTypes }`,
  ...snapshotIds.map(
    (id, i) => `  { id: ${JSON.stringify(id)}, services: v${i}Services, types: v${i}Types }`,
  ),
];

const out = `// Auto-generated by scripts/regen-explorer-versions.mjs. Do not edit by hand.
${imports.join("\n")}
import type { VersionEntry } from "./data-types.js";

export type { VersionEntry } from "./data-types.js";

/// Version string declared in \`js/packages/truapi/package.json\` at codegen
/// time. Mirrors the \`truapi\` crate version. Used by the explorer to render
/// the \`main\` selector label as \`main (x.y.z)\`.
export const packageVersion = ${JSON.stringify(packageVersion)};

export const versions: VersionEntry[] = [
${entries.join(",\n")},
];
`;

writeFileSync(TARGET, out);
console.log(
  `wrote ${TARGET} (main + ${snapshotIds.length} snapshot${snapshotIds.length === 1 ? "" : "s"})`,
);
