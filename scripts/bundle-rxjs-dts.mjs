#!/usr/bin/env node
// Snapshot every .d.ts under node_modules/rxjs/dist/types into a single
// generated TS file that the playground registers with Monaco. We pull the
// real package types instead of redeclaring a hand-rolled subset — that way
// new operators show up automatically and signatures stay correct.

import { readdir, readFile, writeFile, mkdir, stat } from "node:fs/promises";
import { join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = fileURLToPath(new URL("..", import.meta.url));
const RXJS_DIR = join(ROOT, "playground/node_modules/rxjs");
const OUT_DIR = join(ROOT, "playground/src/lib/codegen");
const OUT = join(OUT_DIR, "rxjs-dts.ts");

async function* walk(dir) {
  for (const entry of await readdir(dir, { withFileTypes: true })) {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) yield* walk(path);
    else if (entry.isFile() && path.endsWith(".d.ts")) yield path;
  }
}

function asTemplateLiteral(s) {
  return s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

try {
  await stat(join(RXJS_DIR, "package.json"));
} catch {
  throw new Error(
    `bundle-rxjs-dts: cannot find rxjs at ${RXJS_DIR}. Run \`yarn install\` in playground/ first.`,
  );
}

const TYPES_DIR = join(RXJS_DIR, "dist/types");
const entries = [];
for await (const path of walk(TYPES_DIR)) {
  const rel = relative(RXJS_DIR, path).replace(/\\/g, "/");
  const content = await readFile(path, "utf8");
  entries.push({
    path: `file:///node_modules/rxjs/${rel}`,
    content,
  });
}

// Stub package.json so Monaco's NodeJs resolution finds the right types entry
// when an example writes `import { from } from "rxjs"`.
entries.push({
  path: "file:///node_modules/rxjs/package.json",
  content: JSON.stringify(
    { name: "rxjs", types: "./dist/types/index.d.ts" },
    null,
    2,
  ),
});

const body = entries
  .map(
    (e) =>
      `  { path: ${JSON.stringify(e.path)}, content: \`${asTemplateLiteral(e.content)}\` },`,
  )
  .join("\n");

await mkdir(OUT_DIR, { recursive: true });
await writeFile(
  OUT,
  `// Auto-generated by scripts/bundle-rxjs-dts.mjs. Do not edit.\n` +
    `export const rxjsFiles: { path: string; content: string }[] = [\n${body}\n];\n`,
);

const totalBytes = entries.reduce((acc, e) => acc + e.content.length, 0);
console.log(
  `wrote ${relative(ROOT, OUT)} (${entries.length} files, ${totalBytes} bytes)`,
);
