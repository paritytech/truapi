use super::*;
use indoc::writedoc;

/// Generates playground-only service metadata from the same Rustdoc API input
/// used by the client generator.
pub fn generate_playground_services(
    api: &ApiDefinition,
    output_dir: &str,
    target_version: u32,
) -> Result<()> {
    let codegen_dir = Path::new(output_dir).join("codegen");
    fs::create_dir_all(&codegen_dir)?;
    validate_versioned_wrapper_shapes(api)?;

    let code = generate_playground_services_code(api, target_version)?;
    fs::write(codegen_dir.join("services.ts"), code)?;

    Ok(())
}

fn generate_playground_services_code(api: &ApiDefinition, target_version: u32) -> Result<String> {
    let wrappers = collect_versioned_wrappers(api);
    let emit_versions = versioned_wrapper_emit_versions(api, &wrappers, target_version)?;
    let aliases = selected_public_aliases(api, &wrappers, &emit_versions, target_version);
    let ctx = CodecContext::default();
    let services = public_services(api)?;

    let mut out = String::new();
    writedoc!(
        out,
        r#"
        // Auto-generated by truapi-codegen. Do not edit.
        import type {{ ServiceInfo }} from '../services-types.js';

        export const services: ServiceInfo[] = [
        "#
    )
    .unwrap();

    for service in services {
        let trait_def = service.trait_def;
        let mut methods = included_methods(trait_def, &wrappers, target_version)?;
        methods.sort_by_key(|method| (method_wire_sort_id(method), method.name.as_str()));
        if methods.is_empty() {
            continue;
        }

        writedoc!(
            out,
            "
              {{
                name: {name},
                methods: [
            ",
            name = ts_string_literal(&service_display_name(trait_def)),
        )
        .unwrap();

        for method in methods {
            let wire_version = method_wire_version(method, &wrappers, target_version)?;
            let payload = emit_payload(&method.params, &wrappers, &ctx, wire_version)?;
            let docs = split_playground_docs(method.docs.as_deref(), &method.name)?;
            let method_type = match method.kind {
                MethodKind::Request => "unary",
                MethodKind::Subscription | MethodKind::ResultSubscription => "subscription",
            };

            writedoc!(
                out,
                "
                      {{
                        name: {name},
                        type: {ty},
                ",
                name = ts_string_literal(&method.name),
                ty = ts_string_literal(method_type),
            )
            .unwrap();
            if let Some(description) = docs.description {
                writeln!(
                    out,
                    "        description: {},",
                    ts_string_literal(&description)
                )
                .unwrap();
            }
            let no_params = method.name == "host_handshake" || payload.param_list.is_empty();
            if !no_params {
                let request_description =
                    playground_request_description(api, &aliases, &payload.inner_type_ts);
                writeln!(
                    out,
                    "        requestDescription: {},",
                    ts_string_literal(&request_description)
                )
                .unwrap();
            }
            if no_params {
                writeln!(out, "        noParams: true,").unwrap();
            } else if let Some(default_request) = docs.default_request {
                writeln!(
                    out,
                    "        defaultRequest: {},",
                    ts_string_literal(&default_request)
                )
                .unwrap();
            }
            if let Some(client_example) = docs.client_example.as_deref() {
                let function_name = extract_exported_function_name(client_example).ok_or_else(
                    || {
                        anyhow::anyhow!(
                            "method `{}` has a ```ts example but no `export function` declaration",
                            method.name,
                        )
                    },
                )?;
                writeln!(
                    out,
                    "        exampleSource: {},",
                    ts_string_literal(client_example)
                )
                .unwrap();
                writeln!(
                    out,
                    "        exampleFunctionName: {},",
                    ts_string_literal(&function_name)
                )
                .unwrap();
            }
            writeln!(out, "      }},").unwrap();
        }

        writedoc!(
            out,
            "
                ],
              }},
            ",
        )
        .unwrap();
    }

    writeln!(out, "];").unwrap();

    Ok(out)
}

#[derive(Debug)]
pub(super) struct PlaygroundDocs {
    pub(super) description: Option<String>,
    pub(super) default_request: Option<String>,
    pub(super) client_example: Option<String>,
}

pub(super) fn split_playground_docs(
    docs: Option<&str>,
    method_name: &str,
) -> Result<PlaygroundDocs> {
    let Some(docs) = docs else {
        return Ok(PlaygroundDocs {
            description: None,
            default_request: None,
            client_example: None,
        });
    };

    let mut description = Vec::new();
    let mut client_example = Vec::new();
    let mut in_client_example = false;
    for line in docs.lines() {
        let trimmed = line.trim();
        if trimmed == "```ts" {
            in_client_example = true;
            continue;
        }
        if in_client_example && trimmed == "```" {
            in_client_example = false;
            continue;
        }
        if in_client_example {
            client_example.push(line);
        } else {
            description.push(line);
        }
    }

    let description = trim_doc_lines(&description);
    let client_example = trim_doc_lines(&client_example);
    let default_request = if let Some(client_example) = &client_example {
        extract_default_request_from_client_example(method_name, client_example)?
    } else {
        None
    };

    Ok(PlaygroundDocs {
        description,
        default_request,
        client_example,
    })
}

fn validate_default_request(method_name: &str, source: &str, value: &str) -> Result<()> {
    serde_json::from_str::<serde_json::Value>(value)
        .map_err(|err| anyhow::anyhow!("invalid {source} JSON for `{method_name}`: {err}"))?;
    Ok(())
}

fn extract_default_request_from_client_example(
    method_name: &str,
    example: &str,
) -> Result<Option<String>> {
    let Some(call_start) = example.find("truapi.") else {
        return Ok(None);
    };
    let Some(open_offset) = example[call_start..].find('(') else {
        return Ok(None);
    };
    let open = call_start + open_offset;
    let Some(close) = find_matching_delimiter(example, open, '(', ')') else {
        return Ok(None);
    };
    let argument = example[open + 1..close].trim();
    if argument.is_empty() {
        return Ok(None);
    }

    let request = if argument.starts_with('{') {
        if let Some(request) = extract_request_property(argument) {
            request
        } else if argument.contains("next")
            || argument.contains("error")
            || argument.contains("complete")
        {
            return Ok(None);
        } else {
            argument
        }
    } else if matches!(
        argument.as_bytes().first(),
        Some(b'"' | b'\'' | b'`' | b'[')
    ) {
        argument
    } else {
        return Ok(None);
    };
    let json = ts_request_to_playground_json(request);
    if validate_default_request(method_name, "ts rustdoc example", &json).is_err() {
        return Ok(None);
    }
    let Ok(value) = serde_json::from_str::<serde_json::Value>(&json) else {
        return Ok(None);
    };
    Ok(Some(serde_json::to_string_pretty(&value)?))
}

fn extract_request_property(argument: &str) -> Option<&str> {
    let open = argument.find('{')?;
    let close = find_matching_delimiter(argument, open, '{', '}')?;
    let bytes = argument.as_bytes();
    let mut i = open + 1;
    let mut depth = 1usize;
    let mut quote: Option<u8> = None;
    while i < close {
        let b = bytes[i];
        if let Some(q) = quote {
            if b == q && bytes.get(i.wrapping_sub(1)) != Some(&b'\\') {
                quote = None;
            }
            i += 1;
            continue;
        }
        if matches!(b, b'\'' | b'"' | b'`') {
            quote = Some(b);
            i += 1;
            continue;
        }
        match b {
            b'{' | b'[' | b'(' => {
                depth += 1;
                i += 1;
            }
            b'}' | b']' | b')' => {
                depth = depth.saturating_sub(1);
                i += 1;
            }
            _ if depth == 1 && argument[i..].starts_with("request") => {
                let after_key = i + "request".len();
                let mut colon = after_key;
                while bytes.get(colon).is_some_and(u8::is_ascii_whitespace) {
                    colon += 1;
                }
                if bytes.get(colon) != Some(&b':') {
                    i += 1;
                    continue;
                }
                let mut value_start = colon + 1;
                while bytes.get(value_start).is_some_and(u8::is_ascii_whitespace) {
                    value_start += 1;
                }
                let value_end = find_ts_value_end(argument, value_start);
                return Some(argument[value_start..value_end].trim());
            }
            _ => i += 1,
        }
    }
    None
}

fn find_ts_value_end(input: &str, start: usize) -> usize {
    let bytes = input.as_bytes();
    let mut i = start;
    let mut depth = 0usize;
    let mut quote: Option<u8> = None;
    while i < input.len() {
        let b = bytes[i];
        if let Some(q) = quote {
            if b == q && bytes.get(i.wrapping_sub(1)) != Some(&b'\\') {
                quote = None;
            }
            i += 1;
            continue;
        }
        if matches!(b, b'\'' | b'"' | b'`') {
            quote = Some(b);
            i += 1;
            continue;
        }
        match b {
            b'{' | b'[' | b'(' => {
                depth += 1;
                i += 1;
            }
            b'}' | b']' | b')' => {
                if depth == 0 {
                    return i;
                }
                depth -= 1;
                i += 1;
            }
            b',' if depth == 0 => return i,
            _ => i += 1,
        }
    }
    input.len()
}

fn find_matching_delimiter(
    input: &str,
    open: usize,
    open_ch: char,
    close_ch: char,
) -> Option<usize> {
    let mut depth = 0usize;
    let mut quote: Option<char> = None;
    let mut prev = '\0';
    for (i, ch) in input.char_indices().skip_while(|(i, _)| *i < open) {
        if let Some(q) = quote {
            if ch == q && prev != '\\' {
                quote = None;
            }
            prev = ch;
            continue;
        }
        if matches!(ch, '\'' | '"' | '`') {
            quote = Some(ch);
            prev = ch;
            continue;
        }
        if ch == open_ch {
            depth += 1;
        } else if ch == close_ch {
            depth = depth.checked_sub(1)?;
            if depth == 0 {
                return Some(i);
            }
        }
        prev = ch;
    }
    None
}

fn ts_request_to_playground_json(input: &str) -> String {
    let input = normalize_single_quoted_strings(input);
    let input = quote_unquoted_object_keys(&input);
    let input = quote_bigint_literals(&input);
    let input = remove_undefined_object_properties(&input);
    remove_trailing_commas(&input)
}

/// Rewrite single-quoted TypeScript string literals as double-quoted JSON strings,
/// escaping any inner `"` so that examples like `message: '{"jsonrpc":"2.0"}'`
/// survive the JSON validation downstream.
fn normalize_single_quoted_strings(input: &str) -> String {
    let bytes = input.as_bytes();
    let mut out = String::with_capacity(input.len());
    let mut i = 0usize;
    while i < bytes.len() {
        let b = bytes[i];
        match b {
            b'"' | b'`' => {
                out.push(b as char);
                i += 1;
                while i < bytes.len() {
                    let c = bytes[i];
                    out.push(c as char);
                    i += 1;
                    if c == b && bytes.get(i.wrapping_sub(2)) != Some(&b'\\') {
                        break;
                    }
                }
            }
            b'\'' => {
                out.push('"');
                i += 1;
                while i < bytes.len() {
                    let c = bytes[i];
                    if c == b'\'' && bytes.get(i.wrapping_sub(1)) != Some(&b'\\') {
                        out.push('"');
                        i += 1;
                        break;
                    }
                    if c == b'"' {
                        out.push('\\');
                        out.push('"');
                    } else if c == b'\\' && bytes.get(i + 1) == Some(&b'\'') {
                        out.push('\'');
                        i += 2;
                        continue;
                    } else {
                        out.push(c as char);
                    }
                    i += 1;
                }
            }
            _ => {
                out.push(b as char);
                i += 1;
            }
        }
    }
    out
}

fn remove_undefined_object_properties(input: &str) -> String {
    let bytes = input.as_bytes();
    let mut out = String::new();
    let mut i = 0usize;
    let mut quote: Option<u8> = None;
    while i < input.len() {
        let b = bytes[i];
        if let Some(q) = quote {
            out.push(b as char);
            if b == q && bytes.get(i.wrapping_sub(1)) != Some(&b'\\') {
                quote = None;
            }
            i += 1;
            continue;
        }
        if b == b'"' && input[i..].starts_with("\"value\"") {
            let mut colon = i + "\"value\"".len();
            while bytes.get(colon).is_some_and(u8::is_ascii_whitespace) {
                colon += 1;
            }
            if bytes.get(colon) == Some(&b':') {
                let mut value = colon + 1;
                while bytes.get(value).is_some_and(u8::is_ascii_whitespace) {
                    value += 1;
                }
                if input[value..].starts_with("undefined") {
                    let mut end = value + "undefined".len();
                    while bytes.get(end).is_some_and(u8::is_ascii_whitespace) {
                        end += 1;
                    }
                    if bytes.get(end) == Some(&b',') {
                        end += 1;
                    } else if out.ends_with(',') {
                        out.pop();
                    } else {
                        while out.chars().last().is_some_and(char::is_whitespace) {
                            out.pop();
                        }
                        if out.ends_with(',') {
                            out.pop();
                        }
                    }
                    i = end;
                    continue;
                }
            }
        }
        if matches!(b, b'\'' | b'"' | b'`') {
            quote = Some(b);
            out.push(b as char);
            i += 1;
            continue;
        }
        out.push(b as char);
        i += 1;
    }
    out
}

fn quote_unquoted_object_keys(input: &str) -> String {
    let bytes = input.as_bytes();
    let mut out = String::new();
    let mut i = 0usize;
    let mut quote: Option<u8> = None;
    while i < input.len() {
        let b = bytes[i];
        if let Some(q) = quote {
            out.push(b as char);
            if b == q && bytes.get(i.wrapping_sub(1)) != Some(&b'\\') {
                quote = None;
            }
            i += 1;
            continue;
        }
        if matches!(b, b'\'' | b'"' | b'`') {
            quote = Some(b);
            out.push(b as char);
            i += 1;
            continue;
        }
        if b == b'{' || b == b',' {
            out.push(b as char);
            i += 1;
            while bytes.get(i).is_some_and(u8::is_ascii_whitespace) {
                out.push(bytes[i] as char);
                i += 1;
            }
            let ident_start = i;
            if bytes
                .get(i)
                .is_some_and(|b| b.is_ascii_alphabetic() || *b == b'_')
            {
                i += 1;
                while bytes
                    .get(i)
                    .is_some_and(|b| b.is_ascii_alphanumeric() || *b == b'_')
                {
                    i += 1;
                }
                let mut colon = i;
                while bytes.get(colon).is_some_and(u8::is_ascii_whitespace) {
                    colon += 1;
                }
                if bytes.get(colon) == Some(&b':') {
                    out.push('"');
                    out.push_str(&input[ident_start..i]);
                    out.push('"');
                    continue;
                }
                out.push_str(&input[ident_start..i]);
                continue;
            }
            continue;
        }
        out.push(b as char);
        i += 1;
    }
    out
}

fn quote_bigint_literals(input: &str) -> String {
    let bytes = input.as_bytes();
    let mut out = String::new();
    let mut i = 0usize;
    let mut quote: Option<u8> = None;
    while i < input.len() {
        let b = bytes[i];
        if let Some(q) = quote {
            out.push(b as char);
            if b == q && bytes.get(i.wrapping_sub(1)) != Some(&b'\\') {
                quote = None;
            }
            i += 1;
            continue;
        }
        if matches!(b, b'\'' | b'"' | b'`') {
            quote = Some(b);
            out.push(b as char);
            i += 1;
            continue;
        }
        if b.is_ascii_digit() {
            let start = i;
            i += 1;
            while bytes.get(i).is_some_and(u8::is_ascii_digit) {
                i += 1;
            }
            if bytes.get(i) == Some(&b'n') {
                out.push('"');
                out.push_str(&input[start..=i]);
                out.push('"');
                i += 1;
            } else {
                out.push_str(&input[start..i]);
            }
            continue;
        }
        out.push(b as char);
        i += 1;
    }
    out
}

fn remove_trailing_commas(input: &str) -> String {
    let bytes = input.as_bytes();
    let mut out = String::new();
    let mut i = 0usize;
    let mut quote: Option<u8> = None;
    while i < input.len() {
        let b = bytes[i];
        if let Some(q) = quote {
            out.push(b as char);
            if b == q && bytes.get(i.wrapping_sub(1)) != Some(&b'\\') {
                quote = None;
            }
            i += 1;
            continue;
        }
        if matches!(b, b'\'' | b'"' | b'`') {
            quote = Some(b);
            out.push(b as char);
            i += 1;
            continue;
        }
        if b == b',' {
            let mut next = i + 1;
            while bytes.get(next).is_some_and(u8::is_ascii_whitespace) {
                next += 1;
            }
            if matches!(bytes.get(next), Some(b'}' | b']')) {
                i += 1;
                continue;
            }
        }
        out.push(b as char);
        i += 1;
    }
    out
}

pub(super) fn playground_type_name(value: &str) -> String {
    value.replace("T.", "")
}

fn playground_request_description(
    api: &ApiDefinition,
    aliases: &BTreeMap<String, String>,
    value: &str,
) -> String {
    let value = playground_type_name(value);
    api.types
        .iter()
        .filter(|ty| {
            aliases
                .get(&ty.name)
                .map(String::as_str)
                .unwrap_or(&ty.name)
                == value
        })
        .find_map(|ty| match &ty.kind {
            TypeDefKind::Enum(variants) if is_unit_only_enum(ty) => {
                Some(unit_enum_summary(variants))
            }
            _ => None,
        })
        .unwrap_or(value)
}

pub(super) fn extract_exported_function_name(source: &str) -> Option<String> {
    for line in source.lines() {
        let trimmed = line.trim_start();
        let Some(rest) = trimmed.strip_prefix("export ") else {
            continue;
        };
        let rest = rest.strip_prefix("async ").unwrap_or(rest);
        let Some(rest) = rest.strip_prefix("function ") else {
            continue;
        };
        let name: String = rest
            .chars()
            .take_while(|c| c.is_ascii_alphanumeric() || *c == '_' || *c == '$')
            .collect();
        if !name.is_empty() {
            return Some(name);
        }
    }
    None
}

#[cfg(test)]
mod tests {
    use super::{extract_exported_function_name, ts_request_to_playground_json};
    use serde_json::json;

    #[test]
    fn extract_exported_function_name_matches_named_exports() {
        let src = "import { Client } from \"@parity/truapi\";\n\nexport async function getThing(truapi: Client) {\n  return null;\n}\n";
        assert_eq!(
            extract_exported_function_name(src),
            Some("getThing".to_string()),
        );

        let sync = "export function followX(truapi: Client) { return null; }";
        assert_eq!(
            extract_exported_function_name(sync),
            Some("followX".to_string()),
        );

        assert_eq!(extract_exported_function_name("const x = 1;"), None);
    }

    #[test]
    fn ts_request_to_playground_json_normalizes_ts_style_literals() {
        let input = r#"{
            roomId: 'room-1',
            message: '{"jsonrpc":"2.0","id":1}',
            amount: 42n,
            bytes: "0x",
            optional: {
                value: undefined,
                keep: 'ok',
            },
        }"#;

        let actual = ts_request_to_playground_json(input);
        let parsed: serde_json::Value =
            serde_json::from_str(&actual).expect("normalized request should be valid JSON");

        assert_eq!(
            parsed,
            json!({
                "roomId": "room-1",
                "message": r#"{"jsonrpc":"2.0","id":1}"#,
                "amount": "42n",
                "bytes": "0x",
                "optional": {
                    "keep": "ok"
                }
            })
        );
    }
}
