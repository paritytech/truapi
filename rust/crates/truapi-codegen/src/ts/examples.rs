use super::playground::split_playground_docs;
use super::*;

fn ts_example_file_stem(name: &str) -> String {
    name.to_case(Case::Kebab)
}

/// Prelude injected at the top of every generated example. Declares the
/// ambient `truapi` client; helper functions like `withChainHeadFollow` are
/// declared once in the sibling `__ambient.d.ts` so non-chain examples do
/// not pull them into local scope.
const EXAMPLE_PRELUDE: &str = r#"// Auto-generated by truapi-codegen. Do not edit.
import type { Client } from "@parity/truapi";
declare const truapi: Client;

"#;

/// Shared ambient declarations for every example. Mirrors the runtime
/// helpers injected by `playground/src/lib/example-helpers.ts` so each
/// generated example can reference `withChainHeadFollow` without redefining it.
const EXAMPLE_AMBIENT_DTS: &str = r#"// Auto-generated by truapi-codegen. Do not edit.
import type { Observable } from "rxjs";

declare global {
  /**
   * Per-block context emitted by {@link withChainHeadFollow}. `hash` is
   * the first finalized block hash reported on the underlying
   * `chainHead_follow` subscription, and `followSubscriptionId` is the
   * id that subsequent chain-head requests must reference.
   */
  type ChainHeadCtx = {
    genesisHash: `0x${string}`;
    followSubscriptionId: string;
    hash: `0x${string}`;
  };
  /**
   * Start a `chainHead_follow` subscription and emit a {@link ChainHeadCtx}
   * for every `Initialized` event. The returned Observable completes on
   * `Stop` and errors on `OperationError` and `OperationInaccessible`.
   */
  function withChainHeadFollow(opts: {
    genesisHash: `0x${string}`;
    withRuntime?: boolean;
  }): Observable<ChainHeadCtx>;
}

export {};
"#;

/// Generates standalone TypeScript files for every `ts` rustdoc block so
/// the playground's CI can typecheck them against the published types and
/// the rxjs operators its examples use.
pub fn generate_client_examples(
    api: &ApiDefinition,
    output_dir: &str,
    target_version: u32,
) -> Result<()> {
    let output_path = Path::new(output_dir);
    if output_path.exists() {
        fs::remove_dir_all(output_path)?;
    }
    fs::create_dir_all(output_path)?;
    fs::write(output_path.join("__ambient.d.ts"), EXAMPLE_AMBIENT_DTS)?;
    validate_versioned_wrapper_shapes(api)?;

    let wrappers = collect_versioned_wrappers(api);
    let services = public_services(api)?;

    for service in services {
        let trait_def = service.trait_def;
        let mut methods = included_methods(trait_def, &wrappers, target_version)?;
        methods.sort_by_key(|method| (method_wire_sort_id(method), method.name.as_str()));

        for method in methods {
            let docs = split_playground_docs(method.docs.as_deref())?;
            let Some(client_example) = docs.client_example else {
                continue;
            };
            let filename = format!(
                "{}-{}.ts",
                ts_example_file_stem(&trait_def.name),
                ts_example_file_stem(&method.name)
            );
            let code = format!(
                "{EXAMPLE_PRELUDE}\
                 {client_example}\n\
                 \n\
                 export {{}};\n"
            );
            fs::write(output_path.join(filename), code)?;
        }
    }

    Ok(())
}
