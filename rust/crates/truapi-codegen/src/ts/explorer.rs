use super::*;
use indoc::writedoc;

use super::playground::{name_to_kebab_id, split_playground_docs};

/// Generates `codegen/types.ts` under `output_dir`. The file exports the
/// `DataType[]` list consumed by the explorer site (one entry per public
/// type in the selected client surface). Versioned wrapper enums are not
/// emitted — they are wire-level and the explorer routes around them via
/// the request/response/error type IDs already baked into `MethodInfo`.
pub fn generate_explorer(api: &ApiDefinition, output_dir: &str, target_version: u32) -> Result<()> {
    let codegen_dir = Path::new(output_dir).join("codegen");
    fs::create_dir_all(&codegen_dir)?;

    let code = generate_explorer_types_code(api, target_version)?;
    fs::write(codegen_dir.join("types.ts"), code)?;
    Ok(())
}

fn generate_explorer_types_code(api: &ApiDefinition, target_version: u32) -> Result<String> {
    let wrappers = collect_versioned_wrappers(api);
    let emit_versions = versioned_wrapper_emit_versions(api, &wrappers, target_version)?;
    let aliases = selected_public_aliases(api, &wrappers, &emit_versions, target_version);

    let mut out = String::new();
    writedoc!(
        out,
        r#"
        // Auto-generated by truapi-codegen. Do not edit.
        import type {{ DataType }} from '../data-types.js';

        export const types: DataType[] = [
        "#
    )
    .unwrap();

    // When a versioned alias (e.g. `V01HostFoo` → `HostFoo`) and an
    // unversioned competitor (`HostFoo`) both exist, the alias represents the
    // *selected* surface for the target wire version. Skip any unversioned
    // type whose name collides with an aliased public name unless the
    // unversioned type itself is the chosen alias source.
    let aliased_public_names: BTreeSet<&String> = aliases.values().collect();
    let mut entries: Vec<DataTypeEntry> = Vec::new();
    for ty in &api.types {
        if detect_versioned_wrapper(ty).is_some() {
            continue;
        }
        let is_aliased_source = aliases.contains_key(&ty.name);
        if !is_aliased_source && aliased_public_names.contains(&ty.name) {
            continue;
        }
        let public_name = if let Some(alias) = aliases.get(&ty.name) {
            alias.clone()
        } else {
            ty.name.clone()
        };
        if entries.iter().any(|e| e.name == public_name) {
            continue;
        }

        let id = name_to_kebab_id(&public_name);
        let category = data_type_category(&ty.module_path);
        let definition = data_type_definition(ty, &public_name, &emit_versions, &aliases)?;
        let description = split_playground_docs(ty.docs.as_deref())?.description;
        let (fields, variants) = data_type_fields_variants(ty);

        entries.push(DataTypeEntry {
            id,
            name: public_name,
            category,
            definition,
            description,
            fields,
            variants,
        });
    }

    entries.sort_by(|a, b| a.id.cmp(&b.id));

    for entry in entries {
        writedoc!(
            out,
            "
              {{
                id: {id},
                name: {name},
                category: {category},
                definition: {definition},
            ",
            id = ts_string_literal(&entry.id),
            name = ts_string_literal(&entry.name),
            category = ts_string_literal(&entry.category),
            definition = ts_string_literal(&entry.definition),
        )
        .unwrap();
        if let Some(description) = entry.description.as_deref() {
            writeln!(out, "    description: {},", ts_string_literal(description)).unwrap();
        }
        if let Some(fields) = entry.fields.as_deref() {
            writeln!(out, "    fields: [").unwrap();
            for f in fields {
                write_data_field(&mut out, f);
            }
            writeln!(out, "    ],").unwrap();
        }
        if let Some(variants) = entry.variants.as_deref() {
            writeln!(out, "    variants: [").unwrap();
            for v in variants {
                write_data_field(&mut out, v);
            }
            writeln!(out, "    ],").unwrap();
        }
        writeln!(out, "  }},").unwrap();
    }

    writeln!(out, "];").unwrap();
    Ok(out)
}

struct DataTypeEntry {
    id: String,
    name: String,
    category: String,
    definition: String,
    description: Option<String>,
    fields: Option<Vec<DataField>>,
    variants: Option<Vec<DataField>>,
}

#[derive(Clone)]
struct DataField {
    name: String,
    ts_type: String,
    description: Option<String>,
}

fn write_data_field(out: &mut String, f: &DataField) {
    writedoc!(
        out,
        "
          {{
            name: {name},
            type: {ty},
        ",
        name = ts_string_literal(&f.name),
        ty = ts_string_literal(&f.ts_type),
    )
    .unwrap();
    if let Some(description) = f.description.as_deref() {
        writeln!(
            out,
            "        description: {},",
            ts_string_literal(description)
        )
        .unwrap();
    }
    writeln!(out, "      }},").unwrap();
}

fn data_type_category(module_path: &[String]) -> String {
    let mut iter = module_path.iter();
    if iter.next().is_none() {
        return "shared".into();
    }
    for segment in iter {
        if segment == "api" || segment == "versioned" {
            continue;
        }
        if segment.starts_with('v') && segment[1..].chars().all(|c| c.is_ascii_digit() || c == '_')
        {
            continue;
        }
        return segment.clone();
    }
    "shared".into()
}

fn data_type_definition(
    ty: &TypeDef,
    public_name: &str,
    emit_versions: &HashMap<String, BTreeSet<u32>>,
    aliases: &BTreeMap<String, String>,
) -> Result<String> {
    let generic_decl = generic_param_declaration(&ty.generic_params);
    let mut out = String::new();
    match &ty.kind {
        TypeDefKind::Alias(type_ref) => {
            writeln!(
                out,
                "export type {public_name}{generic_decl} = {};",
                ts_type(type_ref)?
            )
            .unwrap();
        }
        TypeDefKind::Struct(fields) => {
            writeln!(out, "export interface {public_name}{generic_decl} {{").unwrap();
            for field in fields {
                let (ts_name, optional) = ts_field_name(&field.name, &field.type_ref);
                if optional {
                    writeln!(out, "  {ts_name}?: {};", ts_inner_option(&field.type_ref)?).unwrap();
                } else {
                    writeln!(out, "  {ts_name}: {};", ts_type(&field.type_ref)?).unwrap();
                }
            }
            writeln!(out, "}}").unwrap();
        }
        TypeDefKind::TupleStruct(fields) => {
            writeln!(
                out,
                "export type {public_name}{generic_decl} = {};",
                unnamed_fields_type(fields)?
            )
            .unwrap();
        }
        TypeDefKind::Enum(variants) => {
            if is_unit_only_enum(ty) {
                writeln!(
                    out,
                    "export type {public_name}{generic_decl} = {};",
                    unit_enum_union_type(variants)?
                )
                .unwrap();
            } else {
                let selected = emit_versions.get(&ty.name);
                writeln!(out, "export type {public_name}{generic_decl} =").unwrap();
                for variant in variants {
                    if let Some(versions) = selected {
                        let Some(version) = version_number(&variant.name) else {
                            continue;
                        };
                        if !versions.contains(&version) {
                            continue;
                        }
                    }
                    writeln!(out, "  | {}", enum_variant_ts_type(variant)?).unwrap();
                }
                writeln!(out, ";").unwrap();
            }
        }
    }
    let _ = aliases;
    Ok(out.trim_end().to_string())
}

fn data_type_fields_variants(ty: &TypeDef) -> (Option<Vec<DataField>>, Option<Vec<DataField>>) {
    match &ty.kind {
        TypeDefKind::Struct(fields) => {
            let out: Vec<DataField> = fields
                .iter()
                .map(|f| DataField {
                    name: f.name.clone(),
                    ts_type: ts_type(&f.type_ref).unwrap_or_else(|_| "unknown".into()),
                    description: split_playground_docs(f.docs.as_deref())
                        .ok()
                        .and_then(|d| d.description),
                })
                .collect();
            (Some(out), None)
        }
        TypeDefKind::Enum(variants) => {
            let out: Vec<DataField> = variants
                .iter()
                .map(|v| {
                    let ts_type = enum_variant_ts_type(v).unwrap_or_else(|_| "unknown".into());
                    DataField {
                        name: v.name.clone(),
                        ts_type,
                        description: split_playground_docs(v.docs.as_deref())
                            .ok()
                            .and_then(|d| d.description),
                    }
                })
                .collect();
            (None, Some(out))
        }
        _ => (None, None),
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn category_falls_back_to_shared_for_versioned_module() {
        let path = vec!["truapi".into(), "versioned".into()];
        assert_eq!(data_type_category(&path), "shared");
    }

    #[test]
    fn category_falls_back_to_shared_for_v01_only() {
        let path = vec!["truapi".into(), "v01".into()];
        assert_eq!(data_type_category(&path), "shared");
    }

    #[test]
    fn category_uses_first_non_versioning_segment() {
        let path = vec!["truapi".into(), "api".into(), "account".into()];
        assert_eq!(data_type_category(&path), "account");
    }

    #[test]
    fn category_inside_v0n_module() {
        let path = vec!["truapi".into(), "v02".into(), "account".into()];
        assert_eq!(data_type_category(&path), "account");
    }
}
